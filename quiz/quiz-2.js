// Q. 次のデータ型はそれぞれ何か？
// "ピカチュウ"
// 10
// true
// "null"
console.log(typeof "ピカチュウ"); // A.string
console.log(typeof 10); // A.number
console.log(typeof true); // A.boolean
console.log(typeof "null"); // A.string

